class CustomDistance(nn.Module):
    def __init__(self, cell_range=16, crop_size=16, pair_num=30, threshold=3):
        super(CustomDistance, self).__init__()
        self.cell_range = cell_range
        self.crop_size = crop_size
        self.pair_num = pair_num
        self.threshold = threshold
        self.hist_dim = 256 / cell_range
        self.hist_mu = torch.tensor([[[[range(self.hist_dim)]]]]).float()
    def im2col(self, fea):
        tmp = fea.unfold(2, self.crop_size, self.crop_size)
        tmp1 = tmp.unfold(3, self.crop_size, self.crop_size)
        tmp2 = tmp1.reshape((tmp1.shape[0], tmp1.shape[1], tmp1.shape[2]*tmp1.shape[3], tmp1.shape[4]*tmp1.shape[5])) # [b, c, hist_num, hist_dim]
        return tmp2
    def softhist(self, fea):
        fea_tmp = fea.unsqueeze(-1)
        fea_tmp1 = fea_tmp.repeat(1,1,1,1,self.hist_dim) # [b, c, hist_num, hist_dim, hist_dim]
        tmp_mu = self.hist_mu.repeat(fea_tmp1.shape[0], fea_tmp1.shape[1], fea_tmp1.shape[2], fea_tmp1.shape[3], 1)
        tmp = 1 - torch.abs(fea_tmp1 - tmp_mu)
        tmp1 = torch.max(tmp, torch.zeros_like(tmp))
        tmp_sum = torch.sum(tmp1, dim=3) # [b, c, hist_num, hist_dim]
        return tmp_sum
    def gather_abs(self, fea, idx):
        tmp_fea = fea.unsqueeze(3) # [b, c, hist_num, 1, hist_dim]
        tmp_fea_gather = tmp_fea.repeat(1,1,1,fea.shape[2],1)
        tmp_fea_gather = tmp_fea_gather.permute(0,1,3,2,4) # [b, c, hist_num, hist_num, hist_dim]
        tmp_idx = idx.unsqueeze(-1) # [b, 1, hist_num, pair_num, 1]
        tmp_idx = tmp_idx.repeat(1,fea.shape[1],1,1, fea.shape[3]) # [b, c, hist_num, pair_num, hist_dim]
        #tmp_idx = tmp_idx.repeat(1,1,1,1, fea.shape[3]) # [b, 1, hist_num, pair_num, hist_dim]
        tmp_gather = tmp_fea_gather.gather(-2, tmp_idx) # [b, c, hist_num, pair_num, hist_dim]
        tmp_fea_repeat = tmp_fea.repeat(1,1,1,idx.shape[3],1) # [b, c, hist_num, pair_num, hist_dim]
        tmp_abs = torch.abs(tmp_gather - tmp_fea_repeat)
        return tmp_abs # [b, c, hist_num, pair_num, hist_dim]
    def get_idx_in(self, fea):
        fea_batch, fea_channel, fea_height, fea_width = fea.shape
        h_crop_num = (fea_height-1) / self.crop_size +1
        w_crop_num = (fea_width-1) / self.crop_size +1
        hist_num = h_crop_num * w_crop_num
        hist_pair = torch.randint(hist_num, (fea_batch, 1, hist_num, self.pair_num), device=torch.device(fea.device)).long()
        #hist_pair = torch.randint(hist_num, (fea_batch, fea_channel, hist_num, self.pair_num), device=torch.device(fea.device)).long()
        return hist_pair
    def forward(self, fea_in, fea_out):
        self.hist_mu = self.hist_mu.to(fea_out.device)
        idx_in = self.get_idx_in(fea_in)
        rgb_in = ((fea_in +1) *127.5) / self.cell_range
        rgb_out = ((fea_out +1) *127.5) / self.cell_range
        col_in = self.im2col(rgb_in)
        col_out = self.im2col(rgb_out)
        hist_fea_in = self.softhist(col_in)
        hist_fea_out = self.softhist(col_out)
        hist_diff_in = self.gather_abs(hist_fea_in, idx_in)
        hist_diff_out = self.gather_abs(hist_fea_out, idx_in)
        diff_in_mean = torch.mean(hist_diff_in, dim=-1, keepdim=True) # [b, c, hist_num, pair_num, 1]
        diff_in_mean = torch.mean(diff_in_mean, dim=1, keepdim=True) # [b, 1, hist_num, pair_num, 1]
        hist_diff_bool = diff_in_mean <= self.threshold
        hist_diff_bool = hist_diff_bool.repeat(1,1,1,1,hist_diff_out.shape[4])
        hist_diff_bool = hist_diff_bool.repeat(1,hist_diff_out.shape[1],1,1,1)
        hist_diff_res = hist_diff_out[hist_diff_bool]
        #hist_diff_bool = diff_in_mean <= self.threshold
        #hist_diff_out = hist_diff_bool.float() * hist_diff_out
        return torch.mean(hist_diff_res)


